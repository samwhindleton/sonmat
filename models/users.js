// ----------------------------------------
// | DEPENDENCIES                         |
// ----------------------------------------
const mongoose = require('mongoose');


// ----------------------------------------
// | SCHEMA                               |
// ----------------------------------------
// --------------------                   |
// Setup              |                   |
// --------------------                   |
// ----------------------------------------
const userSchema = new mongoose.Schema(
  {
    username: String,
    password: String
  }
);


// ----------------------------------------
// | MODEL                                |
// ----------------------------------------
// --------------------                   |
// Setup              |                   |
// --------------------                   |
// ----------------------------------------
const User = mongoose.model('User', userSchema);


// ----------------------------------------
// | MODULE EXPORTS                       |
// ----------------------------------------
// Access Users in controllers/users.js   |
// ----------------------------------------
module.exports = User;
